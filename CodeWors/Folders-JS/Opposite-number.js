//Very simple, given a number (integer / decimal / both depending on the language), find its opposite (additive inverse).



function opposite(number) {
    if(number > 0 || number < 0){
        return -1* number;
    }
    else{
        return undefined;
    }
}



function opposite(number) {
    return -1* number;
}

function opposite(number) {
    return -number;
}




